static void poll_input(void) {
    const Uint8 *k = SDL_GetKeyboardState(NULL);
    in_left  = k[SDL_SCANCODE_LEFT]  || k[SDL_SCANCODE_A];
    in_right = k[SDL_SCANCODE_RIGHT] || k[SDL_SCANCODE_D];
    in_up    = k[SDL_SCANCODE_UP]    || k[SDL_SCANCODE_W];
    in_down  = k[SDL_SCANCODE_DOWN]  || k[SDL_SCANCODE_S];
    in_jump  = k[SDL_SCANCODE_Z]     || k[SDL_SCANCODE_SPACE];
    in_shoot = k[SDL_SCANCODE_X]     || k[SDL_SCANCODE_LSHIFT];
    in_dash  = k[SDL_SCANCODE_C]     || k[SDL_SCANCODE_V];

    if (joy) {
        Sint16 ax = SDL_JoystickGetAxis(joy, 0);
        Sint16 ay = SDL_JoystickGetAxis(joy, 1);
        if (ax < -8000) in_left = 1;
        if (ax >  8000) in_right = 1;
        if (ay < -8000) in_up = 1;
        if (ay >  8000) in_down = 1;
        if (SDL_JoystickGetButton(joy, VITA_BTN_LEFT))  in_left = 1;
        if (SDL_JoystickGetButton(joy, VITA_BTN_RIGHT)) in_right = 1;
        if (SDL_JoystickGetButton(joy, VITA_BTN_UP))    in_up = 1;
        if (SDL_JoystickGetButton(joy, VITA_BTN_DOWN))  in_down = 1;
        if (SDL_JoystickGetButton(joy, VITA_BTN_CROSS))  in_jump = 1;
        if (SDL_JoystickGetButton(joy, VITA_BTN_SQUARE)) in_shoot = 1;
        if (SDL_JoystickGetButton(joy, VITA_BTN_CIRCLE)) in_dash = 1;
    }
}

/* ---------- update ---------- */
static void fire_one(float ox, float oy, float vx, float vy) {
    for (int i = 0; i < MAX_BULLETS; i++) {
        if (bullets[i].alive) continue;
        bullets[i].x = ox; bullets[i].y = oy;
        bullets[i].vx = vx; bullets[i].vy = vy;
        bullets[i].alive = 1; bullets[i].life = 60;
        return;
    }
}

static void fire_bullet(void) {
    float ox = player.x + PHIT_W / 2.0f, oy = player.y + PHIT_H / 2.0f;
    float vx = 0, vy = 0;
    if (in_up)        vy = -BULLET_SPEED;
    else if (in_down && !player.on_ground) vy = BULLET_SPEED;
    else              vx = BULLET_SPEED * (float)player.facing;
    int vertical = (vy != 0);

    if (level >= 3) {           /* double shot: two parallel bullets */
        if (vertical) { fire_one(ox - 3, oy, vx, vy); fire_one(ox + 3, oy, vx, vy); }
        else          { fire_one(ox, oy - 3, vx, vy); fire_one(ox, oy + 3, vx, vy); }
    } else {
        fire_one(ox, oy, vx, vy);
    }
    if (level >= 6) {           /* triple: add a spread bullet pair */
        if (vertical) { fire_one(ox, oy, -1.2f, vy); fire_one(ox, oy, 1.2f, vy); }
        else          { fire_one(ox, oy, vx, -1.2f); fire_one(ox, oy, vx, 1.2f); }
    }
    muzzle_x = ox + (vertical ? 0 : (float)player.facing * 8);
    muzzle_y = oy + (vertical ? (vy > 0 ? 8 : -8) : 0);
    muzzle_t = 4;
}

static void update_player(void) {
    float speed = MOVE_SPEED + (player.gliding ? 0.5f : 0.0f);
    if (in_left)  { player.vx = -speed; player.facing = -1; }
    else if (in_right) { player.vx = speed; player.facing = 1; }
    else player.vx *= player.gliding ? 0.93f : 0.7f;

    int jump_pressed = in_jump && !jump_prev;
    int dash_pressed = in_dash && !dash_prev;

    /* dash skill (LV2+): horizontal burst with brief i-frames */
    if (player.dash_cd > 0) player.dash_cd--;
    if (dash_pressed && level >= 2 && player.dash_cd == 0 && player.dash == 0) {
        player.dash = 10; player.dash_cd = 45;
        player.dashx = (float)player.facing; player.dashy = 0;
        if (player.inv < 12) player.inv = 12;
        play_sfx(S_DASH);
        spawn_particles(player.x + 6, player.y + 9, 0xFFF5F1E8, 8);
    }

    player.vy += GRAVITY;
    if (player.vy > 6.0f) player.vy = 6.0f;
    /* variable jump height: releasing jump early cuts the rise */
    if (!in_jump && player.vy < -2.0f) player.vy = -2.0f;

    /* glide: hold jump while falling - the tail catches the wind */
    /* held (not freshly pressed) jump while falling engages the glide */
    player.gliding = (!player.on_ground && in_jump && jump_prev &&
                      player.vy > 0.4f && player.dash == 0);
    if (player.gliding) {
        if (player.vy > 0.65f) player.vy = 0.65f;
        if (ticks % 5 == 0)
            spawn_particles(player.x + PHIT_W / 2.0f - player.facing * 6,
                            player.y + 4, 0xAAF5F1E8, 1);
    }

    if (jump_pressed) {
        if (player.on_ground || player.coyote > 0) {
            int fy = (int)((player.y + PHIT_H + 1) / TILE);
            int on_plat = 0, on_hard = 0;
            for (int tx = (int)(player.x / TILE);
                 tx <= (int)((player.x + PHIT_W - 1) / TILE); tx++) {
                char c = map_at(tx, fy);
                if (c == '=') on_plat = 1;
                if (c == '#') on_hard = 1;
            }
            if (in_down && player.on_ground && on_plat && !on_hard) {
                player.drop = 12;
                player.y += 2;
            } else {
                player.vy = JUMP_VEL;
                player.coyote = 0;
                player.jump_t = 8;
                play_sfx(S_JUMP);
                spawn_particles(player.x + PHIT_W / 2.0f, player.y + PHIT_H, 0xFFE7DCC2, 4);
            }
        } else if (player.air_jumps > 0) {
            /* air jump: tuck into a ball and kick off the wind */
            player.air_jumps--;
            player.vy = AIRJUMP_VEL;
            player.spin_t = 22;
            play_sfx(S_FLY);
            spawn_particles(player.x + PHIT_W / 2.0f, player.y + PHIT_H, 0xFFF5F1E8, 6);
        }
    }
    if (player.drop > 0) player.drop--;

    /* dash overrides velocity while active (hangs in the air) */
    if (player.dash > 0) {
        player.dash--;
        player.vx = player.dashx * 5.0f;
        player.vy = 0;
        spawn_particles(player.x + 6, player.y + 9, 0xFFF5F1E8, 1);
        if (player.dash % 3 == 0) {           /* afterimage trail */
            for (int i = 0; i < 6; i++)
                if (ghosts[i].life <= 0) {
                    ghosts[i].x = player.x; ghosts[i].y = player.y;
                    ghosts[i].life = 12; ghosts[i].facing = player.facing;
                    break;
                }
        }
    }

    /* little dust puffs while sprinting on the ground */
    if (player.on_ground && fabsf(player.vx) > 1.2f && ticks % 9 == 0)
        spawn_particles(player.x + PHIT_W / 2.0f - player.facing * 5,
                        player.y + PHIT_H - 1, 0xB0E7DCC2, 1);

    float nx = player.x + player.vx;
    if (!hard_box(nx, player.y, PHIT_W, PHIT_H)) player.x = nx;
    else player.vx = 0;

    int was_airborne = !player.on_ground;
    player.on_ground = 0;
    if (player.vy >= 0) {
        float old_feet = player.y + PHIT_H - 1;
        float ny = player.y + player.vy;
        if (landing_box(player.x, ny, PHIT_W, PHIT_H, old_feet, player.drop == 0)) {
            /* snap feet onto the tile top so the hero never hovers */
            int fty = (int)((ny + PHIT_H - 1) / TILE);
            player.y = (float)(fty * TILE - PHIT_H);
            player.on_ground = 1;
            player.gliding = 0;
            player.air_jumps = 1;
            player.spin_t = 0;
            player.coyote = 6;
            if (was_airborne && player.vy > 3.0f) {
                player.land_t = 8;
                spawn_particles(player.x + PHIT_W / 2.0f, player.y + PHIT_H, 0xFFE7DCC2, 6);
            }
            player.vy = 0;
        } else {
            player.y = ny;
        }
    } else {
        float ny = player.y + player.vy;
        if (!hard_box(player.x, ny, PHIT_W, PHIT_H)) player.y = ny;
        else player.vy = 0;
    }

    if (!player.on_ground && player.coyote > 0) player.coyote--;
    if (player.land_t > 0) player.land_t--;
    if (player.jump_t > 0) player.jump_t--;
    if (player.spin_t > 0) player.spin_t--;

    player.anim += fabsf(player.vx) * 0.15f;

    if (player.shoot_cd > 0) player.shoot_cd--;
    if (in_shoot && !shoot_prev && player.shoot_cd == 0) {
        fire_bullet();
        play_sfx(S_SHOOT);
        player.shoot_cd = (level >= 4) ? 7 : 12;   /* rapid fire skill */
    }

    if (player.inv > 0) player.inv--;
    if (combo_timer > 0) { if (--combo_timer == 0) combo = 0; }
    if (banner_life > 0) banner_life--;
    if (muzzle_t > 0) muzzle_t--;
    if (gem_pop > 0) gem_pop--;
    for (int i = 0; i < 6; i++)
        if (ghosts[i].life > 0) ghosts[i].life--;

    if (player.y > MAP_H * TILE + 60 && fade == 0) {
        spawn_particles(player.x, (float)(MAP_H * TILE), 0xFFC8384A, 12);
        fade = 50;   /* fade to black, respawn at midpoint */
    }

    jump_prev = in_jump;
    shoot_prev = in_shoot;
    dash_prev = in_dash;
}

static void hurt_player(void) {
    if (player.inv > 0 || fade > 0) return;
    player.hp--;
    player.inv = 90;
    player.vy = -3.0f;
    shake = 5;
    freeze = 5;
    play_sfx(S_HURT);
    spawn_particles(player.x + PHIT_W / 2.0f, player.y + PHIT_H / 2.0f, 0xFFC8384A, 8);
    if (player.hp <= 0) fade = 50;
}

static void enemy_die(Enemy *e) {
    float w, h; enemy_size(e, &w, &h);
    e->alive = 0;
    play_sfx(S_KILL);
    shake = e->type == T_BOSS ? 8 : 3;
    freeze = e->type == T_BOSS ? 8 : 3;    /* hit-stop makes kills land */
    player.dash_cd = 0;                    /* kills refresh the dash */
    spawn_fx(e->x + w / 2 - 20, e->y + h / 2 - 20);
    spawn_particles(e->x + w / 2, e->y + h / 2, 0xFF9BA0AB, 12);
    if (e->type == T_BOSS) {
        boss_active = 0;
        add_kill_score(SCORE_BOSS, e->x, e->y - 10);
        show_banner(cur_level == NUM_LEVELS - 1 ? "THE TYRANT FALLS!" : "GENERAL DEFEATED!");
        play_sfx(S_WIN);
        if (score > best_score) best_score = score;
        save_best();
        for (int i = 0; i < 6; i++)
            spawn_particles(e->x + (float)(rand() % 80 - 40),
                            e->y + (float)(rand() % 80 - 40), 0xFFF2CE45, 20);
    } else {
        add_kill_score(e->type == T_EAGLE ? SCORE_EAGLE : SCORE_GROUND,
                       e->x, e->y - 6);
    }
}

static void update_enemies(void) {
    for (int i = 0; i < n_enemies; i++) {
        Enemy *e = &enemies[i];
        if (!e->alive) continue;
        e->bob += 0.05f;
        if (e->flash > 0) e->flash--;
        float w, h; enemy_size(e, &w, &h);
        float pcx = player.x + PHIT_W / 2.0f, pcy = player.y + PHIT_H / 2.0f;
        float ecx = e->x + w / 2, ecy = e->y + h / 2;
        float dx = pcx - ecx, dy = pcy - ecy;
        float dist = sqrtf(dx * dx + dy * dy);

        switch (e->type) {
        case T_EAGLE:
            if (dist < 100.0f && dist > 1.0f) {
                e->vx += (dx / dist) * 0.05f;
                e->vy += (dy / dist) * 0.05f;
            } else {
                e->vx += (e->homex - e->x) * 0.002f;
                e->vy += (e->homey - e->y) * 0.002f;
            }
            e->vx *= 0.96f; e->vy *= 0.96f;
            {   /* cap chase speed so eagles stay dodgeable */
                float sp = sqrtf(e->vx * e->vx + e->vy * e->vy);
                if (sp > 1.7f) { e->vx *= 1.7f / sp; e->vy *= 1.7f / sp; }
            }
            {
                float ex = e->x + e->vx;
                if (!hard_at(ex + w / 2, e->y + h / 2)) e->x = ex;
                else e->vx *= -0.6f;
                float ey = e->y + e->vy + sinf(e->bob) * 0.3f;
                if (!hard_at(e->x + w / 2, ey + h / 2)) e->y = ey;
                else e->vy *= -0.6f;
            }
            break;

        case T_FROG: {
            e->vy += 0.3f;
            if (e->vy > 5) e->vy = 5;
            int grounded = hard_at(ecx, e->y + h + 1);
            if (grounded) {
                e->vx = 0;
                if (e->timer > 0) e->timer--;
                if (e->state == 2) {
                    /* crouched: leap once the telegraph runs out */
                    if (e->timer == 0) {
                        e->vy = -4.4f;
                        e->vx = (dx > 0 ? 1 : -1) * 1.4f;
                        e->timer = 130;
                        e->state = 1;
                    }
                } else {
                    e->state = 0;
                    if (e->timer == 0 && dist < 120.0f) {
                        e->state = 2;      /* crouch telegraph before the hop */
                        e->timer = 26;
                    }
                }
                if (e->vy > 0) e->vy = 0;
            } else e->state = 1;
            float ex = e->x + e->vx;
            if (!hard_box(ex, e->y, w, h)) e->x = ex; else e->vx = 0;
            float ey = e->y + e->vy;
            if (!hard_box(e->x, ey, w, h)) e->y = ey;
            else { if (e->vy > 0) e->state = 0; e->vy = 0; }
            break;
        }

        case T_OPOSSUM: {
            e->vy += 0.3f;
            if (e->vy > 5) e->vy = 5;
            /* walk, flip at walls and ledges */
            float ahead_x = e->vx > 0 ? e->x + w + 2 : e->x - 2;
            if (hard_at(ahead_x, e->y + h / 2) ||
                (!hard_at(ahead_x, e->y + h + 4) && hard_at(ecx, e->y + h + 1)))
                e->vx = -e->vx;
            float ex = e->x + e->vx;
            if (!hard_box(ex, e->y, w, h)) e->x = ex; else e->vx = -e->vx;
            float ey = e->y + e->vy;
            if (!hard_box(e->x, ey, w, h)) e->y = ey; else e->vy = 0;
            break;
        }

        case T_BOSS: {
            if (!boss_active && !victory && dist < 200.0f) {
                boss_active = 1;
                char b[40];
                snprintf(b, sizeof b, "BOSS: %s", BOSS_CFG[e->kind].name);
                show_banner(b);
                play_sfx(S_BOSS);
                shake = 6;
            }
            if (!boss_active) break;
            if (e->timer > 0) e->timer--;

            if (e->kind == 0) {
                /* MIRE KING: giant frog, huge hops at the player */
                e->vy += 0.3f;
                if (e->vy > 5.5f) e->vy = 5.5f;
                int grounded = hard_at(ecx, e->y + h + 1);
                if (grounded) {
                    e->vx *= 0.7f;
                    if (e->timer == 0 && dist < 260.0f) {
                        e->vy = -6.2f;
                        e->vx = (dx > 0 ? 1 : -1) * 2.2f;
                        e->timer = 120;
                        shake = 2;
                    }
                }
            } else if (e->kind == 1) {
                /* RUST FANG: armored opossum, charges along the ground */
                e->vy += 0.3f;
                if (e->vy > 5.5f) e->vy = 5.5f;
                int grounded = hard_at(ecx, e->y + h + 1);
                if (grounded) {
                    float want = (dx > 0 ? 1 : -1) * 2.6f;
                    e->vx += (want - e->vx) * 0.1f;
                    if (e->timer == 0 && fabsf(dx) < 90.0f) {
                        e->vy = -4.6f;      /* pounce */
                        e->timer = 150;
                    }
                }
            } else {
                /* GALE WRAITH / SKY TYRANT: hover, telegraph, swoop */
                float spd = e->kind == 2 ? 3.8f : 3.4f;
                if (e->state == 0) {
                    float txp = e->homex, typ = e->homey - 30 + sinf(e->bob) * 12;
                    e->vx += (txp - e->x) * 0.004f;
                    e->vy += (typ - e->y) * 0.004f;
                    e->vx *= 0.95f; e->vy *= 0.95f;
                    if (e->timer == 0 && dist < 240.0f) {
                        e->state = 2;
                        e->timer = 22;
                    }
                } else if (e->state == 2) {
                    e->vx *= 0.85f; e->vy *= 0.85f;
                    if (e->timer == 0 && dist > 1.0f) {
                        e->state = 1;
                        e->vx = (dx / dist) * spd;
                        e->vy = (dy / dist) * spd;
                        e->timer = 40;
                        shake = 2;
                    }
                } else {
                    if (e->timer == 0) { e->state = 0; e->timer = e->kind == 2 ? 100 : 130; }
                }
            }

            /* minion waves at hp thresholds (matching the boss's brood) */
            {
                int mt = e->kind == 0 ? T_FROG : e->kind == 1 ? T_OPOSSUM : T_EAGLE;
                int thr1 = BOSS_CFG[e->kind].hp * 2 / 3;
                int thr2 = BOSS_CFG[e->kind].hp / 3;
                if (e->minions_spawned == 0 && e->hp <= thr1) {
                    e->minions_spawned = 1;
                    add_enemy(mt, e->x - 30, e->y - 20);
                    add_enemy(mt, e->x + 50, e->y - 20);
                    show_banner("MINIONS!");
                }
                if (e->minions_spawned == 1 && e->hp <= thr2) {
                    e->minions_spawned = 2;
                    add_enemy(mt, e->x - 30, e->y - 20);
                    add_enemy(mt, e->x + 50, e->y - 20);
                    show_banner("MINIONS!");
                }
            }
            {
                float ex = e->x + e->vx;
                if (!hard_box(ex, e->y, w, h)) e->x = ex;
                else e->vx *= -0.6f;
                float ey = e->y + e->vy + (e->kind >= 2 ? sinf(e->bob) * 0.2f : 0);
                if (!hard_box(e->x, ey, w, h)) e->y = ey;
                else { if (e->vy > 0 && e->kind <= 1) e->vy = 0; else e->vy *= -0.6f; }
            }
            break;
        }
        }

        /* stomp: falling onto a foe squashes it and bounces Kip skyward */
        {
            float feet = player.y + PHIT_H;
            /* a glide descent counts too - drifting down onto a foe should
               squash it, not punish the player with contact damage */
            if ((player.vy > 1.0f || (player.gliding && player.vy > 0.3f)) &&
                player.x < e->x + w - 2 && player.x + PHIT_W > e->x + 2 &&
                feet > e->y - 2 && feet < e->y + h * 0.55f) {
                e->hp -= (e->type == T_BOSS) ? 1 : 3;
                e->flash = 8;
                player.vy = in_jump ? -5.8f : -3.8f;   /* hold jump = high bounce */
                player.air_jumps = 1;                  /* stomp refreshes the air jump */
                player.jump_t = 6;
                play_sfx(S_STOMP);
                freeze = 2;
                shake = 2;
                spawn_particles(pcx, e->y, 0xFFF5F1E8, 8);
                if (e->hp <= 0) enemy_die(e);
                continue;                              /* no contact damage this frame */
            }
        }

        /* contact damage: true box overlap with a little forgiveness */
        if (player.x < e->x + w - 3 && player.x + PHIT_W > e->x + 3 &&
            player.y < e->y + h - 3 && player.y + PHIT_H > e->y + 3) {
            if (player.inv == 0 && fade == 0)
                player.vx = (pcx < ecx) ? -2.5f : 2.5f;   /* knock away */
            hurt_player();
        }
    }
}

static void update_bullets(void) {
    for (int i = 0; i < MAX_BULLETS; i++) {
        Bullet *b = &bullets[i];
        if (!b->alive) continue;
        b->x += b->vx; b->y += b->vy;
        if (--b->life <= 0 || hard_at(b->x, b->y)) {
            if (b->life > 0) spawn_particles(b->x, b->y, 0xFFF2CE45, 3);
            b->alive = 0;
            continue;
        }
        for (int j = 0; j < n_enemies; j++) {
            Enemy *e = &enemies[j];
            if (!e->alive) continue;
            if (e->type == T_BOSS && !boss_active) continue;
            float w, h; enemy_size(e, &w, &h);
            if (b->x > e->x && b->x < e->x + w &&
                b->y > e->y && b->y < e->y + h) {
                e->hp--; e->flash = 8;
                e->vx += b->vx * 0.15f; e->vy += b->vy * 0.15f;
                b->alive = 0;
                play_sfx(S_HIT);
                spawn_particles(b->x, b->y, 0xFFF2CE45, 4);
                if (e->hp <= 0) enemy_die(e);
                break;
            }
        }
    }
}

static void update_pickups(void) {
    for (int i = 0; i < n_gems; i++) {
        Gem *g = &gems[i];
        if (!g->alive) continue;
        g->bob += 0.06f;
        /* magnet: nearby gems drift toward the player */
        {
            float dx = (player.x + 6) - (g->x + 7);
            float dy = (player.y + 9) - (g->y + 6);
            float d = sqrtf(dx * dx + dy * dy);
            /* no pull through terrain: midpoint to the player must be clear */
            if (d < 34.0f && d > 1.0f &&
                !hard_at(g->x + 7 + dx / 2, g->y + 6 + dy / 2)) {
                g->x += dx / d * 2.0f;
                g->y += dy / d * 2.0f;
            }
        }
        if (fabsf((player.x + 6) - (g->x + 7)) < 14 &&
            fabsf((player.y + 9) - (g->y + 6)) < 14) {
            g->alive = 0;
            gems_collected++;
            gem_pop = 10;
            play_sfx(S_GEM);
            add_score(SCORE_GEM, g->x, g->y - 4);
            spawn_particles(g->x + 7, g->y + 6, 0xFF3FB8C4, 10);
            if (gems_collected == total_gems)
                show_banner("ALL GEMS FOUND!");
        }
    }
    for (int i = 0; i < n_cherries; i++) {
        Cherry *ch = &cherries[i];
        if (!ch->alive) continue;
        ch->bob += 0.06f;
        if (fabsf((player.x + 6) - (ch->x + 10)) < 14 &&
            fabsf((player.y + 9) - (ch->y + 10)) < 14) {
            ch->alive = 0;
            play_sfx(S_CHERRY);
            add_score(SCORE_CHERRY, ch->x, ch->y - 4);
            if (player.hp < player.hp_max) {
                player.hp++;
                spawn_popup(ch->x, ch->y - 12, 0xFFC8384A, "+1 HP");
            } else {
                spawn_popup(ch->x, ch->y - 12, 0xFF9BA0AB, "HP FULL");
            }
            spawn_particles(ch->x + 10, ch->y + 10, 0xFFC8384A, 10);
        }
    }
    /* door: opens when enough gems are banked and no boss is alive */
    if (door_tx >= 0 && !door_open) {
        int boss_alive = 0;
        for (int i = 0; i < n_enemies; i++)
            if (enemies[i].alive && enemies[i].type == T_BOSS) boss_alive = 1;
        if (gems_collected >= LEVEL_CFG[cur_level].req_gems && !boss_alive) {
            door_open = 1;
            show_banner("THE WAY OPENS!");
            play_sfx(S_DOOR);
            spawn_particles(door_tx * TILE + 8, door_ty * TILE + 8, 0xFFF2CE45, 16);
        }
    }
    if (door_tx >= 0 && door_open && fade == 0 &&
        fabsf((player.x + 6) - (door_tx * TILE + 8)) < 12 &&
        fabsf((player.y + 9) - (door_ty * TILE + 8)) < 18) {
        fade = 50;
        advancing = 1;
        play_sfx(S_ENTER);
    }

    /* checkpoints: touching a sign moves the respawn point there */
    for (int i = 0; i < n_checkpoints; i++) {
        Checkpoint *cp = &checkpoints[i];
        if (cp->active) continue;
        if (fabsf((player.x + 6) - (cp->x + 9)) < 16 &&
            fabsf((player.y + 9) - (cp->y + 8)) < 20) {
            cp->active = 1;
            play_sfx(S_CHECK);
            player.spawnx = (float)cp->x;
            player.spawny = (float)cp->y - 8;
            if (player.hp < player.hp_max) {       /* signs patch you up */
                player.hp = player.hp_max;
                spawn_popup((float)cp->x - 4, (float)cp->y - 24, 0xFFC8384A, "FULL HP");
            }
            spawn_popup((float)cp->x - 8, (float)cp->y - 14, 0xFF69B857, "CHECKPOINT!");
            spawn_particles((float)cp->x + 9, (float)cp->y + 4, 0xFF69B857, 14);
        }
    }
    /* endgame celebration */
    if (victory && ticks % 12 == 0) {
        static const Uint32 cols[4] =
            {0xFFF2CE45, 0xFF3FB8C4, 0xFFC8384A, 0xFF69B857};
        spawn_particles(player.x + (float)(rand() % 200 - 100),
                        player.y + (float)(rand() % 140 - 100),
                        cols[rand() % 4], 10);
    }
}

static void update_particles(void) {
    for (int i = 0; i < MAX_PARTICLES; i++) {
        if (particles[i].life <= 0) continue;
        particles[i].x += particles[i].vx;
        particles[i].y += particles[i].vy;
        particles[i].vy += 0.05f;
        particles[i].life--;
    }
    for (int i = 0; i < N_DUST; i++) {
        dust[i].x += dust[i].vx;
        dust[i].y += dust[i].vy + sinf((float)ticks * 0.02f + i) * 0.05f;
        if (dust[i].x > MAP_W * TILE) dust[i].x = 0;
        if (dust[i].y < 0) dust[i].y = MAP_H * TILE;
        if (dust[i].y > MAP_H * TILE) dust[i].y = 0;
    }
    for (int i = 0; i < N_LEAVES; i++) {
        leaves[i].sway += 0.03f;
        leaves[i].x += sinf(leaves[i].sway) * 0.4f - 0.1f;
        leaves[i].y += leaves[i].vy;
        if (leaves[i].y > cam_y + LOGICAL_H + 20) {
            leaves[i].y = cam_y - 20;
            leaves[i].x = cam_x + (float)(rand() % LOGICAL_W);
        }
    }
    for (int i = 0; i < MAX_FX; i++) {
        if (!fxs[i].active) continue;
        if (ticks % 4 == 0 && ++fxs[i].frame >= 6) fxs[i].active = 0;
    }
    for (int i = 0; i < MAX_POPUPS; i++) {
        if (popups[i].life <= 0) continue;
        popups[i].y -= 0.4f;
        popups[i].life--;
    }
}

static void update_camera(void) {
    float tx = player.x + PHIT_W / 2.0f - LOGICAL_W / 2.0f;
    float ty = player.y + PHIT_H / 2.0f - LOGICAL_H / 2.0f;
    cam_x += (tx - cam_x) * 0.1f;
    cam_y += (ty - cam_y) * 0.1f;
    if (cam_x < 0) cam_x = 0;
    if (cam_y < 0) cam_y = 0;
    float maxx = (float)(MAP_W * TILE - LOGICAL_W);
    float maxy = (float)(MAP_H * TILE - LOGICAL_H);
    if (cam_x > maxx) cam_x = maxx;
    if (cam_y > maxy) cam_y = maxy;
    if (shake > 0) shake *= 0.85f;
}

/* ---------- render ---------- */
static float g_shx, g_shy;
static void update_shake_offset(void) {
    g_shx = shake > 0.3f ? (float)(rand() % 5 - 2) * shake * 0.3f : 0;
    g_shy = shake > 0.3f ? (float)(rand() % 5 - 2) * shake * 0.3f : 0;
}

static void draw_backdrop(void) {
    const LevelCfg *cfg = &LEVEL_CFG[cur_level];
    SDL_SetTextureColorMod(tex_back, cfg->br, cfg->bg, cfg->bb);
    int dw = back_w * LOGICAL_H / back_h;
    int ox = -(int)fmodf(cam_x * 0.08f, (float)dw);
    for (int x = ox - dw; x < LOGICAL_W + dw; x += dw) {
        SDL_Rect d = {x, 0, dw, LOGICAL_H};
        SDL_RenderCopy(g_ren, tex_back, NULL, &d);
    }
}

static void draw_jungle(float factor, float scale, Uint8 alpha, float ybase) {
    int dw = (int)(mid_w * scale), dh = (int)(mid_h * scale);
    SDL_SetTextureAlphaMod(tex_middle, alpha);
    /* foreground pass sets its own color mod; layers get the level tint */
    const LevelCfg *cfg = &LEVEL_CFG[cur_level];
    if (factor < 1.0f)
        SDL_SetTextureColorMod(tex_middle, cfg->jr, cfg->jg, cfg->jb);
    int ox = -(int)fmodf(cam_x * factor, (float)dw);
    int oy = (int)(ybase - cam_y * factor * 0.3f);
    for (int x = ox - dw; x < LOGICAL_W + dw; x += dw) {
        SDL_Rect d = {x, oy, dw, dh};
        SDL_RenderCopy(g_ren, tex_middle, NULL, &d);
    }
}

/* warm haze band at the horizon between jungle layers */
static void draw_haze(void) {
    SDL_SetTextureColorMod(tex_glow, 255, 240, 200);
    SDL_SetTextureAlphaMod(tex_glow, 26);
    SDL_Rect d = {-100, 70 - (int)(cam_y * 0.1f), LOGICAL_W + 200, 140};
    SDL_RenderCopy(g_ren, tex_glow, NULL, &d);
}

/* diagonal god rays, slowly pulsing */
static void draw_god_rays(void) {
    SDL_SetTextureColorMod(tex_glow, 255, 245, 210);
    for (int i = 0; i < 4; i++) {
        float pulse = sinf((float)ticks * 0.008f + (float)i * 1.7f);
        Uint8 a = (Uint8)(12 + 7 * (pulse + 1));
        SDL_SetTextureAlphaMod(tex_glow, a);
        int x = (int)((float)(90 + i * 120) - fmodf(cam_x * 0.25f, 120.0f));
        SDL_Rect d = {x, -60, 46, 400};
        SDL_RenderCopyEx(g_ren, tex_glow, NULL, &d, 18, NULL, SDL_FLIP_NONE);
    }
}

static void draw_decor(void) {
    float sx = g_shx, sy = g_shy;
    for (int i = 0; i < n_decor; i++) {
        Decor *d = &decor[i];
        SDL_Rect dst;
        SDL_Texture *t;
        switch (d->kind) {
            case 0:  t = tex_tree;   dst.w = 105; dst.h = 93;
                     dst.x = d->x - 44; dst.y = d->y - 93; break;
            case 1:  t = tex_bush;   dst.w = 46;  dst.h = 28;
                     dst.x = d->x - 15; dst.y = d->y - 28; break;
            default: t = tex_shroom; dst.w = 16;  dst.h = 15;
                     dst.x = d->x;     dst.y = d->y - 15; break;
        }
        dst.x += (int)(sx - cam_x); dst.y += (int)(sy - cam_y);
        if (dst.x + dst.w < 0 || dst.x >= LOGICAL_W) continue;
        SDL_RenderCopy(g_ren, t, NULL, &dst);
    }
}

static void draw_map(void) {
    float sx = g_shx, sy = g_shy;
    int x0 = (int)(cam_x / TILE), y0 = (int)(cam_y / TILE);
    for (int ty = y0; ty <= y0 + LOGICAL_H / TILE + 1; ty++) {
        for (int tx = x0; tx <= x0 + LOGICAL_W / TILE + 1; tx++) {
            char c = map_at(tx, ty);
            if (c != '#' && c != '=') continue;
            SDL_Rect dst = {(int)(tx * TILE - cam_x + sx),
                            (int)(ty * TILE - cam_y + sy), TILE, TILE};
            if (c == '=') {
                SDL_RenderCopy(g_ren, tex_platform, NULL, &dst);
            } else {
                const SDL_Rect *src;
                unsigned hash = ((unsigned)tx * 73856093u) ^ ((unsigned)ty * 19349663u);
                hash = (hash >> 13) ^ hash;
                if (map_at(tx, ty - 1) != '#' && map_at(tx, ty - 1) != '=')
                    src = &TS_GRASS[hash % 3];
                else if (hash % 4 == 0)
                    src = &TS_DIRT_VAR[(hash / 7) % 3];
                else
                    src = &TS_DIRT_PLAIN;
                SDL_RenderCopy(g_ren, tex_tileset, src, &dst);
            }
        }
    }
}

/* Enemy breeds: later isles field tougher-looking strains of the same
   animal, so the cast reads as escalating instead of repeating. */
static const Uint8 BREED_TINT[4][3] = {
    {255, 255, 255},   /* isles 1-2: common stock  */
    {170, 215, 255},   /* isles 3-5: frostbitten   */
    {255, 185, 140},   /* isles 6-8: emberbacked   */
    {215, 160, 255},   /* isles 9-10: rift-touched */
};
static int enemy_breed(void) {
    if (cur_level <= 1) return 0;
    if (cur_level <= 4) return 1;
    if (cur_level <= 7) return 2;
    return 3;
}

/* soft blob under an entity, shrinking with height above the ground */
static void draw_shadow(float cx, float feet_y, float wpx) {
    int tx = (int)(cx / TILE), ty = (int)(feet_y / TILE);
    int gy = -1;
    for (int i = 0; i < 6; i++) {
        char c = map_at(tx, ty + i);
        if (c == '#' || c == '=') { gy = (ty + i) * TILE; break; }
    }
    if (gy < 0) return;
    float dist = (float)gy - feet_y;
    if (dist < 0) dist = 0;
    float sc = 1.0f - dist / 96.0f;
    if (sc <= 0.15f) return;
    SDL_SetTextureAlphaMod(tex_shadow, (Uint8)(80 * sc));
    SDL_Rect d = {(int)(cx - wpx * sc / 2 - cam_x + g_shx),
                  (int)(gy - 3 - cam_y + g_shy), (int)(wpx * sc), 6};
    SDL_RenderCopy(g_ren, tex_shadow, NULL, &d);
}

static void draw_enemy(Enemy *e) {
    float w, h; enemy_size(e, &w, &h);
    int flip = (player.x + 6 > e->x + w / 2);
    const Uint8 *bt_c = BREED_TINT[enemy_breed()];
    /* elite breeds carry a faint aura so they pop against the foliage */
    if (e->type != T_BOSS && enemy_breed() > 0) {
        SDL_SetTextureColorMod(tex_glow, bt_c[0], bt_c[1], bt_c[2]);
        SDL_SetTextureAlphaMod(tex_glow, 45);
        SDL_Rect au = {(int)(e->x - cam_x + g_shx) + (int)w / 2 - 22,
                       (int)(e->y - cam_y + g_shy) + (int)h / 2 - 22, 44, 44};
        SDL_RenderCopy(g_ren, tex_glow, NULL, &au);
    }
    switch (e->type) {
    case T_EAGLE: {
        SDL_Rect src = {(int)((ticks / 6 + (long)(e - enemies)) % 4) * 40, 0, 40, 41};
        if (e->flash > 0) SDL_SetTextureColorMod(tex_eagle, 255, 80, 80);
        else SDL_SetTextureColorMod(tex_eagle, bt_c[0], bt_c[1], bt_c[2]);
        SDL_Rect d = {(int)(e->x - cam_x + g_shx) - 12,
                      (int)(e->y - cam_y + g_shy) - 12, 40, 41};
        /* face flight direction while moving; face the player when hovering */
        int eflip = fabsf(e->vx) > 0.3f ? (e->vx > 0) : flip;
        SDL_RenderCopyEx(g_ren, tex_eagle, &src, &d, 0, NULL,
            eflip ? SDL_FLIP_HORIZONTAL : SDL_FLIP_NONE);
        break;
    }
    case T_FROG: {
        /* idle frames 1-3 are the croak balloon: only shown while the frog
           puffs up to telegraph its hop - calm frogs stay on frame 0 */
        SDL_Texture *t; SDL_Rect src;
        if (e->state == 1) {
            t = tex_frog_jump;
            src = (SDL_Rect){(int)(ticks / 8 % 3) * 35, 0, 35, 32};
        } else {
            int fi = 0;
            if (e->state == 2) {
                fi = 3 - e->timer / 9;
                if (fi < 1) fi = 1;
                if (fi > 3) fi = 3;
            }
            t = tex_frog_idle;
            src = (SDL_Rect){fi * 35, 0, 35, 32};
        }
        if (e->flash > 0) SDL_SetTextureColorMod(t, 255, 80, 80);
        else SDL_SetTextureColorMod(t, bt_c[0], bt_c[1], bt_c[2]);
        SDL_Rect d = {(int)(e->x - cam_x + g_shx) - 8,
                      (int)(e->y - cam_y + g_shy) - 12, 35, 32};
        /* frog art faces left */
        SDL_RenderCopyEx(g_ren, t, &src, &d, 0, NULL,
            flip ? SDL_FLIP_HORIZONTAL : SDL_FLIP_NONE);
        break;
    }
    case T_OPOSSUM: {
        SDL_Rect src = {(int)(ticks / 6 % 6) * 36, 0, 36, 28};
        if (e->flash > 0) SDL_SetTextureColorMod(tex_opossum, 255, 80, 80);
        else SDL_SetTextureColorMod(tex_opossum, bt_c[0], bt_c[1], bt_c[2]);
        SDL_Rect d = {(int)(e->x - cam_x + g_shx) - 6,
                      (int)(e->y - cam_y + g_shy) - 14, 36, 28};
        /* opossum art walks left; flip when moving right */
        SDL_RenderCopyEx(g_ren, tex_opossum, &src, &d, 0, NULL,
            e->vx > 0 ? SDL_FLIP_HORIZONTAL : SDL_FLIP_NONE);
        break;
    }
    case T_BOSS: {
        /* menacing glow + big tinted sprite, per boss kind */
        static const Uint8 GLOW_C[4][3] = {{80,255,80},{255,160,40},{190,80,255},{255,60,60}};
        static const Uint8 TINT_C[4][3] = {{170,255,170},{255,190,130},{210,140,255},{255,110,110}};
        const Uint8 *gc = GLOW_C[e->kind], *tc = TINT_C[e->kind];
        float bw, bh; enemy_size(e, &bw, &bh);
        SDL_SetTextureColorMod(tex_glow, gc[0], gc[1], gc[2]);
        SDL_SetTextureAlphaMod(tex_glow, boss_active ? 90 : 40);
        SDL_Rect gl = {(int)(e->x - cam_x + g_shx) + (int)bw / 2 - 45,
                       (int)(e->y - cam_y + g_shy) + (int)bh / 2 - 45, 90, 90};
        SDL_RenderCopy(g_ren, tex_glow, NULL, &gl);

        SDL_Texture *bt; SDL_Rect src_r; SDL_Rect d;
        if (e->kind == 0) {            /* MIRE KING: giant frog */
            int air = !hard_at(e->x + bw / 2, e->y + bh + 1);
            bt = air ? tex_frog_jump : tex_frog_idle;
            int fi;
            if (air) fi = (int)(ticks / 8 % 3);
            else if (boss_active && e->timer < 20) {
                fi = 1 + (19 - e->timer) / 7;      /* puff = leap telegraph */
                if (fi > 3) fi = 3;
            } else fi = 0;
            src_r = (SDL_Rect){fi * 35, 0, 35, 32};
            d = (SDL_Rect){(int)(e->x - cam_x + g_shx) - 18,
                           (int)(e->y - cam_y + g_shy) - 30, 84, 77};
        } else if (e->kind == 1) {     /* RUST FANG: giant opossum */
            bt = tex_opossum;
            src_r = (SDL_Rect){(int)(ticks / 6 % 6) * 36, 0, 36, 28};
            d = (SDL_Rect){(int)(e->x - cam_x + g_shx) - 17,
                           (int)(e->y - cam_y + g_shy) - 31, 86, 67};
        } else {                       /* eagle generals */
            bt = tex_eagle;
            src_r = (SDL_Rect){(int)((ticks / 7) % 4) * 40, 0, 40, 41};
            d = (SDL_Rect){(int)(e->x - cam_x + g_shx) - 20,
                           (int)(e->y - cam_y + g_shy) - 21, 80, 82};
        }
        if (e->flash > 0) SDL_SetTextureColorMod(bt, 255, 255, 255);
        else if (e->state == 2 && (ticks / 3) % 2)
            SDL_SetTextureColorMod(bt, 255, 240, 240);
        else SDL_SetTextureColorMod(bt, tc[0], tc[1], tc[2]);
        int flip_boss;
        if (e->kind == 1) flip_boss = e->vx > 0;      /* opossum walks left */
        else flip_boss = flip;
        SDL_RenderCopyEx(g_ren, bt, &src_r, &d, 0, NULL,
            flip_boss ? SDL_FLIP_HORIZONTAL : SDL_FLIP_NONE);
        SDL_SetTextureColorMod(bt, 255, 255, 255);
        break;
    }
    }
}

static void draw_entities(void) {
    float sx = g_shx, sy = g_shy;

    SDL_SetRenderDrawBlendMode(g_ren, SDL_BLENDMODE_BLEND);
    SDL_SetRenderDrawColor(g_ren, 255, 255, 240, 80);
    for (int i = 0; i < N_DUST; i++) {
        int dxp = (int)(dust[i].x - cam_x), dyp = (int)(dust[i].y - cam_y);
        if (dxp < 0 || dxp >= LOGICAL_W || dyp < 0 || dyp >= LOGICAL_H) continue;
        SDL_Rect d = {dxp, dyp, 1, 1};
        SDL_RenderFillRect(g_ren, &d);
    }

    /* falling leaves */
    for (int i = 0; i < N_LEAVES; i++) {
        int lx = (int)(leaves[i].x - cam_x), ly = (int)(leaves[i].y - cam_y);
        if (lx < -4 || lx >= LOGICAL_W || ly < -4 || ly >= LOGICAL_H) continue;
        set_color((leaves[i].color & 0xFFFFFF) | 0xC8000000);
        int wide = ((int)(leaves[i].sway * 2) % 2) == 0;
        SDL_Rect d = {lx, ly, wide ? 3 : 2, wide ? 2 : 3};
        SDL_RenderFillRect(g_ren, &d);
    }

    /* ground shadows under the cast */
    if (game_state != ST_TITLE && game_state != ST_STORY) {
        draw_shadow(player.x + PHIT_W / 2.0f, player.y + PHIT_H, 16);
        for (int i = 0; i < n_enemies; i++) {
            if (!enemies[i].alive) continue;
            float w, h; enemy_size(&enemies[i], &w, &h);
            draw_shadow(enemies[i].x + w / 2, enemies[i].y + h, w + 4);
        }
    }

    /* checkpoint signs (green tint once activated) */
    for (int i = 0; i < n_checkpoints; i++) {
        Checkpoint *cp = &checkpoints[i];
        if (cp->active) SDL_SetTextureColorMod(tex_sign, 150, 255, 150);
        else SDL_SetTextureColorMod(tex_sign, 255, 255, 255);
        SDL_Rect d = {(int)(cp->x - cam_x + sx),
                      (int)(cp->y - cam_y + sy) - 4, 18, 20};
        SDL_RenderCopy(g_ren, tex_sign, NULL, &d);
    }

    /* the exit door (locked: dim; open: bright + glow) */
    if (door_tx >= 0) {
        SDL_Texture *dt = door_open ? tex_door_open : tex_door;
        if (!door_open) SDL_SetTextureColorMod(dt, 140, 140, 160);
        else SDL_SetTextureColorMod(dt, 255, 255, 255);
        SDL_Rect d = {(int)(door_tx * TILE - cam_x + sx) - 3,
                      (int)(door_ty * TILE - cam_y + sy) + TILE - 33, 22, 33};
        SDL_RenderCopy(g_ren, dt, NULL, &d);
        if (!door_open && game_state == ST_PLAY) {
            char t[24];
            int boss_alive = 0;
            for (int i = 0; i < n_enemies; i++)
                if (enemies[i].alive && enemies[i].type == T_BOSS) boss_alive = 1;
            if (boss_alive)
                snprintf(t, sizeof t, "DEFEAT THE BOSS");
            else
                snprintf(t, sizeof t, "GEMS %d/%d", gems_collected,
                         LEVEL_CFG[cur_level].req_gems);
            draw_text((int)(door_tx * TILE - cam_x) - text_w(t, 1) / 2 + 8,
                      (int)(door_ty * TILE - cam_y) - 26, 1, 0xDDF5F1E8, t);
        }
    }

    /* tutorial hints near the spawn meadow (held back until the isle
       intro card clears so the two never overlap) */
    if (cur_level == 0 && game_state == ST_PLAY && intro_life == 0) {
        draw_text((int)(40 - cam_x), (int)(330 - cam_y), 1, 0xCCF5F1E8, "Z: JUMP - PRESS AGAIN IN AIR");
        draw_text((int)(40 - cam_x), (int)(340 - cam_y), 1, 0xCC9BA0AB, "P / START: PAUSE");
        draw_text((int)(130 - cam_x), (int)(345 - cam_y), 1, 0xCCF5F1E8, "HOLD Z IN AIR: TAIL GLIDE");
        draw_text((int)(130 - cam_x), (int)(355 - cam_y), 1, 0xCC9BE3EA, "ON PLATFORM: DOWN+Z = DROP");
        draw_text((int)(230 - cam_x), (int)(330 - cam_y), 1, 0xCCE8853A, "LAND ON FOES TO STOMP THEM");
    }

    for (int i = 0; i < n_gems; i++) {
        if (!gems[i].alive) continue;
        SDL_Rect src = {(int)(gems[i].bob * 2) % 5 * 15, 0, 15, 13};
        SDL_Rect d = {(int)(gems[i].x - cam_x + sx),
                      (int)(gems[i].y - cam_y + sy + sinf(gems[i].bob) * 2.0f), 15, 13};
        SDL_RenderCopy(g_ren, tex_gem, &src, &d);
    }

    for (int i = 0; i < n_cherries; i++) {
        if (!cherries[i].alive) continue;
        SDL_Rect src = {(int)(cherries[i].bob * 2) % 5 * 21, 0, 21, 21};
        SDL_Rect d = {(int)(cherries[i].x - cam_x + sx),
                      (int)(cherries[i].y - cam_y + sy + sinf(cherries[i].bob) * 2.0f), 21, 21};
        SDL_RenderCopy(g_ren, tex_cherry, &src, &d);
    }

    for (int i = 0; i < n_enemies; i++)
        if (enemies[i].alive) draw_enemy(&enemies[i]);

    for (int i = 0; i < MAX_FX; i++) {
        if (!fxs[i].active) continue;
        SDL_Rect src = {fxs[i].frame * 40, 0, 40, 41};
        SDL_Rect d = {(int)(fxs[i].x - cam_x + sx), (int)(fxs[i].y - cam_y + sy), 40, 41};
        SDL_RenderCopy(g_ren, tex_death, &src, &d);
    }

    set_color(0xFFF2CE45);
    for (int i = 0; i < MAX_BULLETS; i++) {
        if (!bullets[i].alive) continue;
        SDL_Rect d = {(int)(bullets[i].x - cam_x + sx) - 2,
                      (int)(bullets[i].y - cam_y + sy) - 2, 4, 4};
        SDL_RenderFillRect(g_ren, &d);
    }

    for (int i = 0; i < MAX_PARTICLES; i++) {
        if (particles[i].life <= 0) continue;
        set_color(particles[i].color);
        SDL_Rect d = {(int)(particles[i].x - cam_x + sx),
                      (int)(particles[i].y - cam_y + sy), 2, 2};
        SDL_RenderFillRect(g_ren, &d);
    }

    /* dash afterimages: fading copies of the dash pose along the path */
    for (int i = 0; i < 6; i++) {
        if (ghosts[i].life <= 0) continue;
        SDL_SetTextureAlphaMod(tex_hero_jump, (Uint8)(ghosts[i].life * 14));
        SDL_SetTextureColorMod(tex_hero_jump, 180, 220, 255);
        SDL_Rect gsrc = {0, 0, 33, 32};
        SDL_Rect gd = {(int)(ghosts[i].x - cam_x + sx) + PHIT_W / 2 - 16,
                       (int)(ghosts[i].y - cam_y + sy) + PHIT_H - 32, 33, 32};
        SDL_RenderCopyEx(g_ren, tex_hero_jump, &gsrc, &gd, 0, NULL,
            ghosts[i].facing < 0 ? SDL_FLIP_HORIZONTAL : SDL_FLIP_NONE);
    }
    SDL_SetTextureAlphaMod(tex_hero_jump, 255);
    SDL_SetTextureColorMod(tex_hero_jump, 255, 255, 255);

    /* A1: no physics runs on the title/story screens, so Kip would hang
       frozen mid-air there - draw him only in actual play states */
    if (game_state != ST_TITLE && game_state != ST_STORY &&
        player.inv % 8 < 5) {
        /* Kip the Glider Courier (Sunny Land fox, CC0). This sheet has real
           rise/fall poses - the old squirrel jump sheet was a 4-frame spin
           loop, so every airborne frame drew an unreadable orange ball. */
        SDL_Texture *sheet;
        SDL_Rect src_r;
        int fw = 33, fh = 32;
        double angle = 0;
        if (!player.on_ground) {
            sheet = tex_hero_jump;
            int fi = player.vy < 0 ? 0 : 1;          /* rising / falling */
            if (player.spin_t > 0) {
                fi = 0;                              /* air-jump barrel roll */
                angle = (22 - player.spin_t) * 32.0 * player.facing;
            } else if (player.gliding) {
                fi = 1;                              /* tail out, wind-caught */
                angle = 10.0 * player.facing;
            }
            src_r = (SDL_Rect){fi * fw, 0, fw, fh};
        } else if (fabsf(player.vx) > 0.3f) {
            sheet = tex_hero_run;
            src_r = (SDL_Rect){(int)player.anim % 6 * fw, 0, fw, fh};
        } else {
            sheet = tex_hero_idle;
            src_r = (SDL_Rect){(int)((float)ticks * 0.12f) % 4 * fw, 0, fw, fh};
        }
        int dw = fw, dh = fh;
        if (player.land_t > 0)      { dw = fw + player.land_t; dh = fh - player.land_t / 2; }
        else if (player.jump_t > 0) { dw = fw - player.jump_t / 2; dh = fh + player.jump_t / 2; }
        SDL_Rect d = {(int)(player.x - cam_x + sx) + PHIT_W / 2 - dw / 2,
                      (int)(player.y - cam_y + sy) + PHIT_H - dh, dw, dh};
        SDL_RendererFlip flip_p =
            player.facing < 0 ? SDL_FLIP_HORIZONTAL : SDL_FLIP_NONE;
        /* 1px dark outline so Kip reads against busy foliage */
        SDL_SetTextureColorMod(sheet, 20, 16, 30);
        SDL_SetTextureAlphaMod(sheet, 180);
        for (int o = 0; o < 4; o++) {
            static const int ox[4] = {-1, 1, 0, 0}, oy[4] = {0, 0, -1, 1};
            SDL_Rect od = {d.x + ox[o], d.y + oy[o], d.w, d.h};
            SDL_RenderCopyEx(g_ren, sheet, &src_r, &od, angle, NULL, flip_p);
        }
        SDL_SetTextureColorMod(sheet, 255, 255, 255);
        SDL_SetTextureAlphaMod(sheet, 255);
        /* fox art faces right; mirror when heading left */
        SDL_RenderCopyEx(g_ren, sheet, &src_r, &d, angle, NULL, flip_p);
    }

    /* floating score popups */
    for (int i = 0; i < MAX_POPUPS; i++) {
        if (popups[i].life <= 0) continue;
        Uint32 col = popups[i].color;
        if (popups[i].life < 20) col = (col & 0xFFFFFF) | 0x80000000;
        draw_text((int)(popups[i].x - cam_x), (int)(popups[i].y - cam_y), 1,
                  col, popups[i].txt);
    }
}

