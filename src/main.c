/* ---------- progression ---------- */
static void add_score(int pts, float x, float y) {
    score += pts;
    if (pts > 0) {              /* pts==0 is just a level-up recheck */
        char t[16];
        snprintf(t, sizeof t, "+%d", pts);
        spawn_popup(x, y, 0xFFF2CE45, t);
    }

    int new_level = 1;
    for (int i = 0; i < 5; i++)
        if (score >= LEVEL_XP[i]) new_level = i + 2;
    while (level < new_level) {
        level++;
        switch (level) {
            case 2: show_banner("LV2: DASH UNLOCKED!"); break;
            case 3: show_banner("LV3: DOUBLE SHOT!"); break;
            case 4: show_banner("LV4: RAPID FIRE!"); break;
            case 5: show_banner("LV5: MAX HP UP!");
                    player.hp_max = 4; player.hp = player.hp_max; break;
            case 6: show_banner("LV6: TRIPLE SHOT!"); break;
        }
        shake = 3;
        play_sfx(S_LEVEL);
        spawn_particles(player.x + 6, player.y + 9, 0xFFF2CE45, 20);
    }
}

static void add_kill_score(int base, float x, float y) {
    kills++;
    if (combo_timer > 0) combo++;
    else combo = 1;
    combo_timer = 150;
    int pts = base * combo;
    if (combo > 1) {
        char t[16];
        snprintf(t, sizeof t, "+%d X%d", pts, combo);
        score += pts;
        spawn_popup(x, y, 0xFFE8853A, t);
        /* still run level-up check */
        add_score(0, -1000, -1000);
    } else {
        add_score(pts, x, y);
    }
}

/* ---------- enemies ---------- */
static void enemy_size(const Enemy *e, float *w, float *h) {
    switch (e->type) {
        case T_FROG:    *w = 20; *h = 20; break;
        case T_OPOSSUM: *w = 24; *h = 14; break;
        case T_BOSS:
            switch (e->kind) {
                case 0:  *w = 48; *h = 44; break;   /* MIRE KING (frog) */
                case 1:  *w = 52; *h = 36; break;   /* RUST FANG (opossum) */
                default: *w = 40; *h = 40; break;   /* eagle bosses */
            }
            break;
        default:        *w = 16; *h = 16; break;
    }
}

static Enemy *add_enemy(int type, float x, float y) {
    Enemy *e = NULL;
    for (int i = 0; i < n_enemies; i++)
        if (!enemies[i].alive) { e = &enemies[i]; break; }
    if (!e) {
        if (n_enemies >= MAX_ENEMIES) return NULL;
        e = &enemies[n_enemies++];
    }
    memset(e, 0, sizeof *e);
    e->x = e->homex = x; e->y = e->homey = y;
    e->alive = 1; e->type = type;
    e->bob = (float)(rand() % 100);
    switch (type) {
        case T_FROG:    e->hp = 2; break;
        case T_OPOSSUM: e->hp = 2; e->vx = 0.7f; break;
        case T_BOSS:
            e->kind = LEVEL_CFG[cur_level].boss_kind;
            if (e->kind < 0) e->kind = 3;
            e->hp = BOSS_CFG[e->kind].hp;
            break;
        default:        e->hp = 3; break;
    }
    return e;
}

/* ---------- init ---------- */
static void load_level(int idx) {
    cur_level = idx;
    door_tx = door_ty = -1;
    door_open = 0;
    boss_active = 0;
    n_enemies = n_gems = n_cherries = n_decor = n_checkpoints = 0;
    total_gems = gems_collected = 0;
    for (int y = 0; y < MAP_H; y++) {
        int len = (int)strlen(LEVELS[cur_level][y]);
        for (int x = 0; x < len; x++) {
            char c = LEVELS[cur_level][y][x];
            float px = (float)(x * TILE), py = (float)(y * TILE);
            if (c == 'P') {
                player.spawnx = px; player.spawny = py - 8;
                player.x = player.spawnx; player.y = player.spawny;
            } else if (c == 'E') add_enemy(T_EAGLE, px, py);
            else if (c == 'F') add_enemy(T_FROG, px, py);
            else if (c == 'O') add_enemy(T_OPOSSUM, px, py);
            else if (c == 'B') add_enemy(T_BOSS, px, py);
            else if (c == '*' && n_gems < MAX_GEMS) {
                Gem *g = &gems[n_gems++];
                g->x = px; g->y = py + 2; g->alive = 1;
                g->bob = (float)(rand() % 100);
                total_gems++;
            } else if (c == 'D') {
                door_tx = x; door_ty = y;
            } else if (c == 'C' && n_checkpoints < MAX_CHECKPOINTS) {
                checkpoints[n_checkpoints].x = (int)px;
                checkpoints[n_checkpoints].y = (int)py;
                checkpoints[n_checkpoints].active = 0;
                n_checkpoints++;
            } else if (c == 'H' && n_cherries < MAX_CHERRIES) {
                Cherry *ch = &cherries[n_cherries++];
                ch->x = px; ch->y = py; ch->alive = 1;
                ch->bob = (float)(rand() % 100);
            } else if (c == '#' && map_at(x, y - 1) == ' ' && n_decor < MAX_DECOR) {
                int hash = x * 31 + y * 17;
                if (hash % 11 == 0)      { decor[n_decor].kind = 0; goto place; }
                else if (hash % 7 == 3)  { decor[n_decor].kind = 1; goto place; }
                else if (hash % 5 == 2)  { decor[n_decor].kind = 2; goto place; }
                continue;
place:
                decor[n_decor].x = x * TILE;
                decor[n_decor].y = y * TILE;
                n_decor++;
            }
        }
    }
    for (int i = 0; i < N_DUST; i++) {
        dust[i].x = (float)(rand() % (MAP_W * TILE));
        dust[i].y = (float)(rand() % (MAP_H * TILE));
        dust[i].vx = 0.1f + (float)(rand() % 10) / 40.0f;
        dust[i].vy = -0.05f + (float)(rand() % 10) / 100.0f;
    }
    static const Uint32 leaf_cols[4] = {0xFF69B857, 0xFF9BC24A, 0xFFE8853A, 0xFFD4586A};
    for (int i = 0; i < N_LEAVES; i++) {
        leaves[i].x = (float)(rand() % (MAP_W * TILE));
        leaves[i].y = (float)(rand() % (MAP_H * TILE));
        leaves[i].vy = 0.2f + (float)(rand() % 12) / 30.0f;
        leaves[i].sway = (float)(rand() % 100);
        leaves[i].color = leaf_cols[rand() % 4];
    }
    player.hp = player.hp_max;
    player.inv = 60;   /* short grace after entering an isle */
    player.gliding = 0; player.air_jumps = 1; player.spin_t = 0;
    player.vx = player.vy = 0; player.facing = 1; player.anim = 0;
    player.dash = player.dash_cd = 0;
    combo = 0; combo_timer = 0;
    intro_life = 170;                  /* isle name card */
    /* clear leftover projectiles/popups/fx from the previous isle */
    memset(bullets, 0, sizeof bullets);
    memset(popups, 0, sizeof popups);
    memset(fxs, 0, sizeof fxs);
    memset(ghosts, 0, sizeof ghosts);
    muzzle_t = 0;
}

