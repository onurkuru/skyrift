/* ---------- texture loading ---------- */
static SDL_Texture *load_png(const char *name, int *out_w, int *out_h) {
    char path[600];
#ifdef __vita__
    snprintf(path, sizeof path, ASSET_DIR "%s", name);
#else
    snprintf(path, sizeof path, "%sassets/%s", asset_base, name);
#endif
    int w, h, n;
    unsigned char *data = stbi_load(path, &w, &h, &n, 4);
    if (!data) {
        fprintf(stderr, "failed to load %s: %s\n", path, stbi_failure_reason());
        exit(1);
    }
    SDL_Surface *s = SDL_CreateRGBSurfaceWithFormatFrom(
        data, w, h, 32, w * 4, SDL_PIXELFORMAT_RGBA32);
    SDL_Texture *t = SDL_CreateTextureFromSurface(g_ren, s);
    SDL_FreeSurface(s);
    stbi_image_free(data);
    if (out_w) *out_w = w;
    if (out_h) *out_h = h;
    return t;
}

static SDL_Texture *make_sprite(const char **rows, int w, int h) {
    Uint32 *pix = malloc(sizeof(Uint32) * w * h);
    for (int y = 0; y < h; y++) {
        int len = (int)strlen(rows[y]);
        for (int x = 0; x < w; x++)
            pix[y * w + x] = (x < len) ? palette(rows[y][x]) : 0;
    }
    SDL_Surface *s = SDL_CreateRGBSurfaceWithFormatFrom(
        pix, w, h, 32, w * 4, SDL_PIXELFORMAT_ARGB8888);
    SDL_Texture *t = SDL_CreateTextureFromSurface(g_ren, s);
    SDL_FreeSurface(s);
    free(pix);
    return t;
}

static SDL_Texture *make_glow(int size) {
    Uint32 *pix = malloc(sizeof(Uint32) * size * size);
    float r = size / 2.0f;
    for (int y = 0; y < size; y++)
        for (int x = 0; x < size; x++) {
            float dx = x - r + 0.5f, dy = y - r + 0.5f;
            float d = sqrtf(dx * dx + dy * dy) / r;
            float a = d >= 1.0f ? 0 : (1.0f - d) * (1.0f - d);
            Uint8 v = (Uint8)(a * 255);
            pix[y * size + x] = ((Uint32)v << 24) | 0xFFFFFF;
        }
    SDL_SetHint(SDL_HINT_RENDER_SCALE_QUALITY, "1");
    SDL_Surface *s = SDL_CreateRGBSurfaceWithFormatFrom(
        pix, size, size, 32, size * 4, SDL_PIXELFORMAT_ARGB8888);
    SDL_Texture *t = SDL_CreateTextureFromSurface(g_ren, s);
    SDL_SetHint(SDL_HINT_RENDER_SCALE_QUALITY, "0");
    SDL_FreeSurface(s);
    free(pix);
    SDL_SetTextureBlendMode(t, SDL_BLENDMODE_ADD);
    return t;
}

/* soft black blob for ground shadows (alpha-blended, unlike the additive glow) */
static SDL_Texture *make_shadow(int size) {
    Uint32 *pix = malloc(sizeof(Uint32) * size * size);
    float r = size / 2.0f;
    for (int y = 0; y < size; y++)
        for (int x = 0; x < size; x++) {
            float dx = x - r + 0.5f, dy = y - r + 0.5f;
            float d = sqrtf(dx * dx + dy * dy) / r;
            float a = d >= 1.0f ? 0 : (1.0f - d);
            pix[y * size + x] = ((Uint32)(Uint8)(a * 255) << 24) | 0x000000;
        }
    SDL_Surface *s = SDL_CreateRGBSurfaceWithFormatFrom(
        pix, size, size, 32, size * 4, SDL_PIXELFORMAT_ARGB8888);
    SDL_Texture *t = SDL_CreateTextureFromSurface(g_ren, s);
    SDL_FreeSurface(s);
    free(pix);
    SDL_SetTextureBlendMode(t, SDL_BLENDMODE_BLEND);
    return t;
}

static SDL_Texture *make_vignette(void) {
    int w = LOGICAL_W, h = LOGICAL_H;
    Uint32 *pix = malloc(sizeof(Uint32) * w * h);
    float cx = w / 2.0f, cy = h / 2.0f;
    float maxd = sqrtf(cx * cx + cy * cy);
    for (int y = 0; y < h; y++)
        for (int x = 0; x < w; x++) {
            float dx = (x - cx) / maxd, dy = (y - cy) / maxd;
            float d = sqrtf(dx * dx + dy * dy);
            float a = d < 0.55f ? 0 : (d - 0.55f) / 0.45f;
            if (a > 1) a = 1;
            Uint8 v = (Uint8)(a * a * 110);
            pix[y * w + x] = ((Uint32)v << 24) | 0x0A1020;
        }
    SDL_SetHint(SDL_HINT_RENDER_SCALE_QUALITY, "1");
    SDL_Surface *s = SDL_CreateRGBSurfaceWithFormatFrom(
        pix, w, h, 32, w * 4, SDL_PIXELFORMAT_ARGB8888);
    SDL_Texture *t = SDL_CreateTextureFromSurface(g_ren, s);
    SDL_SetHint(SDL_HINT_RENDER_SCALE_QUALITY, "0");
    SDL_FreeSurface(s);
    free(pix);
    SDL_SetTextureBlendMode(t, SDL_BLENDMODE_BLEND);
    return t;
}

/* ---------- text ---------- */
static void set_color(Uint32 argb) {
    SDL_SetRenderDrawColor(g_ren,
        (argb >> 16) & 0xFF, (argb >> 8) & 0xFF, argb & 0xFF, (argb >> 24) & 0xFF);
}

static void draw_glyph(int x, int y, int s, char c) {
    const Uint8 *rows = NULL;
    Uint8 special[5];
    if (c >= '0' && c <= '9') rows = FONT3X5[c - '0'];
    else if (c >= 'A' && c <= 'Z') rows = FONT3X5[c - 'A' + 10];
    else if (c >= 'a' && c <= 'z') rows = FONT3X5[c - 'a' + 10];
    else {
        Uint8 sp[5] = {0,0,0,0,0};
        switch (c) {
            case '+': sp[1]=2; sp[2]=7; sp[3]=2; break;
            case '-': sp[2]=7; break;
            case '=': sp[1]=7; sp[3]=7; break;
            case '!': sp[0]=2; sp[1]=2; sp[2]=2; sp[4]=2; break;
            case ':': sp[1]=2; sp[3]=2; break;
            case '.': sp[4]=2; break;
            case '/': sp[0]=1; sp[1]=1; sp[2]=2; sp[3]=4; sp[4]=4; break;
            default: return;
        }
        memcpy(special, sp, 5); rows = special;
    }
    for (int ry = 0; ry < 5; ry++)
        for (int rx = 0; rx < 3; rx++)
            if (rows[ry] & (4 >> rx)) {
                SDL_Rect d = {x + rx * s, y + ry * s, s, s};
                SDL_RenderFillRect(g_ren, &d);
            }
}

static void draw_text(int x, int y, int s, Uint32 color, const char *t) {
    SDL_SetRenderDrawBlendMode(g_ren, SDL_BLENDMODE_BLEND);
    /* drop shadow for readability */
    set_color(0xC0101018);
    for (const char *p = t; *p; p++)
        draw_glyph(x + (int)(p - t) * 4 * s + s, y + s, s, *p);
    set_color(color);
    for (const char *p = t; *p; p++)
        draw_glyph(x + (int)(p - t) * 4 * s, y, s, *p);
}

static int text_w(const char *t, int s) { return (int)strlen(t) * 4 * s - s; }

/* ---------- map helpers ---------- */
static char map_at(int tx, int ty) {
    if (tx < 0 || tx >= MAP_W || ty < 0 || ty >= MAP_H) return ' ';
    const char *row = LEVELS[cur_level][ty];
    return (tx < (int)strlen(row)) ? row[tx] : ' ';
}

static int hard_at(float px, float py) {
    return map_at((int)(px / TILE), (int)(py / TILE)) == '#';
}

static int hard_box(float x, float y, float w, float h) {
    return hard_at(x, y) || hard_at(x + w - 1, y) ||
           hard_at(x, y + h - 1) || hard_at(x + w - 1, y + h - 1) ||
           hard_at(x + w / 2, y) || hard_at(x + w / 2, y + h - 1);
}

static int landing_box(float x, float y, float w, float h,
                       float old_feet, int allow_oneway) {
    float px[3] = {x, x + w / 2, x + w - 1};
    int ty = (int)((y + h - 1) / TILE);
    for (int i = 0; i < 3; i++) {
        char c = map_at((int)(px[i] / TILE), ty);
        if (c == '#') return 1;
        if (c == '=' && allow_oneway && old_feet <= (float)(ty * TILE)) return 1;
    }
    return 0;
}

