/* ---------- physics smoke tests (SKYRIFT_TEST=1) ----------
   platform in LEVEL: '=' at row 21, cols 8-11 -> world y 336, x 128..191 */
static int run_tests(void) {
    int fail = 0;
    game_state = ST_PLAY;
    in_left = in_right = in_up = in_down = in_jump = in_shoot = in_dash = 0;
    jump_prev = shoot_prev = dash_prev = 0;

    player.x = 150; player.y = 350; player.vy = -4; player.gliding = 0;
    player.on_ground = 0; player.drop = 0;
    update_player();
    if (player.y >= 350) { printf("FAIL pass-through-up: y=%.1f\n", player.y); fail++; }
    else printf("ok  pass-through-up (y 350 -> %.1f)\n", player.y);

    player.x = 150; player.y = 318; player.vy = 6; player.gliding = 0;
    player.on_ground = 0; player.drop = 0;
    update_player();
    if (!player.on_ground || player.vy != 0) { printf("FAIL land-from-above\n"); fail++; }
    else printf("ok  land-from-above (on_ground=%d)\n", player.on_ground);

    in_down = 1; in_jump = 1; jump_prev = 0;
    update_player();
    in_down = in_jump = 0;
    for (int i = 0; i < 12; i++) update_player();
    if (player.y <= 336) { printf("FAIL drop-through: y=%.1f\n", player.y); fail++; }
    else printf("ok  drop-through (y -> %.1f)\n", player.y);

    Enemy *e = &enemies[0];
    memset(e, 0, sizeof *e);
    e->alive = 1; e->type = T_EAGLE;
    e->x = e->homex = 100; e->y = e->homey = 388;
    e->vx = 0; e->vy = 3; e->bob = 0; e->hp = 3;
    player.x = 2000; player.y = 2000;
    for (int i = 0; i < 20; i++) {
        e->vy = 3;
        update_enemies();
        if (e->y < 388) break;
    }
    if (hard_at(e->x + 8, e->y + 8)) { printf("FAIL eagle ghosted into ground\n"); fail++; }
    else printf("ok  eagle-bounce (stayed out of terrain, y=%.1f)\n", e->y);

    /* glide: holding jump while falling caps fall speed */
    player.x = 150; player.y = 250; player.vy = 5; player.on_ground = 0;
    player.dash = 0;
    in_jump = 1; jump_prev = 1;
    update_player();
    if (player.vy > 0.7f) { printf("FAIL glide: vy=%.2f\n", player.vy); fail++; }
    else printf("ok  glide (vy 5 -> %.2f)\n", player.vy);
    in_jump = 0; jump_prev = 0;

    /* air jump: fresh press in the air lifts once */
    player.x = 150; player.y = 250; player.vy = 2; player.on_ground = 0;
    player.air_jumps = 1; player.coyote = 0;
    in_jump = 1; jump_prev = 0;
    update_player();
    if (player.vy >= 0 || player.air_jumps != 0)
        { printf("FAIL air-jump: vy=%.2f jumps=%d\n", player.vy, player.air_jumps); fail++; }
    else printf("ok  air-jump (vy -> %.2f)\n", player.vy);
    in_jump = 0; jump_prev = 0;

    /* skill unlock: score to LV3 must enable double shot */
    player.x = 150; player.y = 300;
    add_score(700, player.x, player.y);
    if (level < 3) { printf("FAIL level-up: level=%d\n", level); fail++; }
    else printf("ok  level-up (score 700 -> LV%d)\n", level);
    int before = 0, after = 0;
    for (int i = 0; i < MAX_BULLETS; i++) if (bullets[i].alive) before++;
    fire_bullet();
    for (int i = 0; i < MAX_BULLETS; i++) if (bullets[i].alive) after++;
    if (after - before != 2) { printf("FAIL double-shot: %d bullets\n", after - before); fail++; }
    else printf("ok  double-shot (%d bullets)\n", after - before);

    /* stomp: falling onto a frog squashes it and bounces Kip */
    e = &enemies[0];
    memset(e, 0, sizeof *e);
    e->alive = 1; e->type = T_FROG; e->hp = 2;
    e->x = 146; e->y = 380; e->homex = e->x; e->homey = e->y;   /* on the ground */
    player.x = 148; player.y = 363; player.vy = 3; player.on_ground = 0;
    player.air_jumps = 0; player.inv = 0;
    update_enemies();
    if (e->alive || player.vy >= 0 || player.air_jumps != 1)
        { printf("FAIL stomp: alive=%d vy=%.1f jumps=%d\n",
                 e->alive, player.vy, player.air_jumps); fail++; }
    else printf("ok  stomp (frog squashed, bounce vy=%.1f, air jump back)\n", player.vy);

    /* full jump + air jump must clear a 5-tile climb (80px): the maps
       are validated against exactly this envelope by genlevels.py */
    player.x = 150; player.y = 300; player.vy = 0; player.on_ground = 1;
    player.air_jumps = 1; player.coyote = 0; player.drop = 0; player.dash = 0;
    {
        float top = 300;
        int air_done = 0;
        in_jump = 1; jump_prev = 0;
        update_player();                      /* ground jump */
        for (int i = 0; i < 120; i++) {
            if (!air_done && player.vy > -0.5f) { jump_prev = 0; air_done = 1; }
            else jump_prev = 1;
            in_jump = 1;
            update_player();
            if (player.y < top) top = player.y;
            if (air_done && player.vy > 1.0f) break;
        }
        if (300 - top < 80.0f)
            { printf("FAIL double-jump height: %.1fpx\n", 300 - top); fail++; }
        else printf("ok  double-jump height (%.1fpx >= 80px)\n", 300 - top);
    }
    in_jump = 0; jump_prev = 0;

    printf(fail ? "TESTS FAILED: %d\n" : "ALL TESTS PASSED\n", fail);
    return fail;
}

/* ---------- main ---------- */
int main(int argc, char *argv[]) {
    (void)argc; (void)argv;
    if (SDL_Init(SDL_INIT_VIDEO | SDL_INIT_JOYSTICK | SDL_INIT_AUDIO) != 0) {
        fprintf(stderr, "SDL_Init: %s\n", SDL_GetError());
        return 1;
    }
    SDL_Window *win = SDL_CreateWindow("Skyrift",
        SDL_WINDOWPOS_CENTERED, SDL_WINDOWPOS_CENTERED,
        WINDOW_W, WINDOW_H, 0);
    g_ren = SDL_CreateRenderer(win, -1,
        SDL_RENDERER_ACCELERATED | SDL_RENDERER_PRESENTVSYNC);
    SDL_RenderSetLogicalSize(g_ren, LOGICAL_W, LOGICAL_H);
    SDL_SetHint(SDL_HINT_RENDER_SCALE_QUALITY, "0");

    if (SDL_NumJoysticks() > 0) joy = SDL_JoystickOpen(0);

#ifndef __vita__
    char *bp = SDL_GetBasePath();
    snprintf(asset_base, sizeof asset_base, "%s", bp ? bp : "./");
    if (bp) SDL_free(bp);
#endif

    tex_eagle       = load_png("eagle.png", NULL, NULL);
    tex_hero_idle   = load_png("player-idle.png", NULL, NULL);
    tex_hero_run    = load_png("player-run.png", NULL, NULL);
    tex_hero_jump   = load_png("player-jump.png", NULL, NULL);
    tex_frog_idle   = load_png("frog-idle.png", NULL, NULL);
    tex_frog_jump   = load_png("frog-jump.png", NULL, NULL);
    tex_opossum     = load_png("opossum.png", NULL, NULL);
    tex_death       = load_png("death.png", NULL, NULL);
    tex_gem         = load_png("gem.png", NULL, NULL);
    tex_cherry      = load_png("cherry.png", NULL, NULL);
    tex_tileset     = load_png("tileset.png", NULL, NULL);
    tex_back        = load_png("back.png", &back_w, &back_h);
    tex_middle      = load_png("middle.png", &mid_w, &mid_h);
    tex_tree        = load_png("tree.png", NULL, NULL);
    tex_bush        = load_png("bush.png", NULL, NULL);
    tex_shroom      = load_png("shrooms.png", NULL, NULL);
    tex_platform    = load_png("platform.png", NULL, NULL);
    tex_sign        = load_png("sign.png", NULL, NULL);
    tex_door        = load_png("door.png", NULL, NULL);
    tex_door_open   = load_png("door-opened.png", NULL, NULL);
    tex_heart       = make_sprite(SPR_HEART, 8, 8);
    tex_glow        = make_glow(64);
    tex_shadow      = make_shadow(48);
    tex_vignette    = make_vignette();

    audio_init();
    load_best();
    game_reset();

    if (getenv("SKYRIFT_TEST")) return run_tests();
    if (getenv("SKYRIFT_SHOT") && !getenv("SKYRIFT_TITLE"))
        game_state = ST_PLAY;   /* dev shots skip the title unless asked */
    if (getenv("SKYRIFT_LEVEL")) {
        int lv = atoi(getenv("SKYRIFT_LEVEL")) % NUM_LEVELS;
        if (lv < 0) lv += NUM_LEVELS;   /* C modulo keeps the sign */
        load_level(lv);
    }

    cam_x = player.x - LOGICAL_W / 2.0f;
    cam_y = player.y - LOGICAL_H / 2.0f;

    int running = 1;
    Uint32 prev = SDL_GetTicks();
    float acc = 0;

    while (running) {
        SDL_Event ev;
        while (SDL_PollEvent(&ev)) {
            if (ev.type == SDL_QUIT) running = 0;
            /* quit needs two steps mid-run: first ESC/SELECT pauses,
               a second one quits - no more losing a run to a stray press */
            if (ev.type == SDL_KEYDOWN && ev.key.keysym.sym == SDLK_ESCAPE) {
                if (game_state == ST_PLAY) game_state = ST_PAUSE;
                else running = 0;
            }
            int pausable = game_state == ST_PLAY || game_state == ST_PAUSE;
            if (ev.type == SDL_KEYDOWN && ev.key.keysym.sym == SDLK_p && pausable)
                game_state = (game_state == ST_PAUSE) ? ST_PLAY : ST_PAUSE;
            if (ev.type == SDL_JOYBUTTONDOWN) {
                if (ev.jbutton.button == VITA_BTN_START && pausable)
                    game_state = (game_state == ST_PAUSE) ? ST_PLAY : ST_PAUSE;
                if (ev.jbutton.button == VITA_BTN_SELECT) {
                    if (game_state == ST_PLAY) game_state = ST_PAUSE;
                    else running = 0;
                }
            }
        }

        Uint32 now = SDL_GetTicks();
        acc += (float)(now - prev);
        prev = now;
        if (acc > 100) acc = 100;

        while (acc >= 1000.0f / 60.0f) {
            poll_input();
            if (game_state == ST_TITLE) {
                /* animated backdrop only; jump goes to the story */
                if (in_jump && !jump_prev) { game_state = ST_STORY; play_sfx(S_START); }
                jump_prev = in_jump;
                update_particles();
            } else if (game_state == ST_STORY) {
                if (in_jump && !jump_prev) {
                    game_state = ST_PLAY;
                    play_sfx(S_START);
                    intro_life = 170;      /* isle card handles the reveal */
                }
                jump_prev = in_jump;
                update_particles();
            } else if (game_state == ST_END) {
                if (in_jump && !jump_prev) {
                    game_reset();
                    game_state = ST_TITLE;
                }
                jump_prev = in_jump;
                update_particles();
            } else if (game_state == ST_PLAY) {
                if (fade > 0) {
                    fade--;
                    if (fade == 25) {
                        if (advancing) {
                            advancing = 0;
                            if (shards < 9) shards++;   /* nine shards, ten doors */
                            /* persist the record each isle: a Vita closed from
                               the LiveArea never reaches the exit-time save */
                            if (score > best_score) best_score = score;
                            save_best();
                            if (cur_level + 1 >= NUM_LEVELS) {
                                victory = 1;
                                if (score > best_score) best_score = score;
                                save_best();
                                play_sfx(S_WIN);
                                game_state = ST_END;
                                fade = 0;
                            } else {
                                load_level(cur_level + 1);
                                cam_x = player.x - LOGICAL_W / 2.0f;
                                cam_y = player.y - LOGICAL_H / 2.0f;
                            }
                        } else respawn();
                    }
                    update_particles();
                } else if (freeze > 0) {
                    freeze--;                     /* hit-stop: world holds a beat */
                } else {
                    update_player();
                    update_enemies();
                    update_bullets();
                    update_pickups();
                    update_particles();
                }
                if (intro_life > 0) intro_life--;
                update_camera();
                play_ticks++;
            }
            ticks++;
            acc -= 1000.0f / 60.0f;
        }

        update_shake_offset();
        draw_backdrop();
        draw_jungle(0.25f, 0.55f, 140, 80);
        draw_haze();
        draw_jungle(0.50f, 0.80f, 220, 40);
        draw_decor();
        draw_map();
        draw_god_rays();
        draw_entities();
        draw_glow_pass();
        draw_foreground();
        SDL_RenderCopy(g_ren, tex_vignette, NULL, NULL);
        if (game_state == ST_TITLE) draw_title();
        else if (game_state == ST_STORY) draw_story();
        else if (game_state == ST_END) draw_end();
        else if (game_state == ST_PAUSE) draw_pause();
        else if (!getenv("SKYRIFT_NOHUD")) draw_hud();

        /* death fade to black and back */
        if (fade > 0) {
            Uint8 a = fade > 25 ? (Uint8)((50 - fade) * 10)
                                : (Uint8)(fade * 10);
            draw_overlay_dim(a);
        }

        if (getenv("SKYRIFT_SHOT") && ticks > 90) {
            int ow, oh;
            SDL_GetRendererOutputSize(g_ren, &ow, &oh);
            SDL_Surface *shot = SDL_CreateRGBSurfaceWithFormat(
                0, ow, oh, 32, SDL_PIXELFORMAT_ARGB8888);
            SDL_RenderReadPixels(g_ren, NULL, SDL_PIXELFORMAT_ARGB8888,
                shot->pixels, shot->pitch);
            SDL_SaveBMP(shot, getenv("SKYRIFT_SHOT"));
            SDL_FreeSurface(shot);
            running = 0;
        }

        SDL_RenderPresent(g_ren);
    }

    save_best();
    if (joy) SDL_JoystickClose(joy);
    SDL_DestroyRenderer(g_ren);
    SDL_DestroyWindow(win);
    SDL_Quit();
    return 0;
}
